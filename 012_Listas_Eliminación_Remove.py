#Elimina de la siguiente lista los elementos 'amarillo' y 'rojo'. Solo puedes eliminarlos utilizando el método remove().

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja'] #se crea lista en la varible colores
colores.remove('amarillo')# de la variable "colores" se elimina de la lista "amarillo" 
colores.remove('rojo') # de la variable "colores" se elimina de la lista "rojo" 
print(colores)#imprime la lista de colores sin los ya eliminados "amarillo y rojo"
